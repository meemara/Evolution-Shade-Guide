export function getFabricsByOpacity(opacityType) {
  return FABRICS.filter(fabric => {
    switch (opacityType) {
      case "sheer":
        return fabric.type === "sheer";
      case "translucent":
        return fabric.type === "translucent";
      case "roomdarkening":
        return fabric.type === "roomdarkening";
      case "blackout":
        return fabric.type === "blackout";
      default:
        return true;
    }
  });
}

export function getFabricsByCollection(collection) {
  return FABRICS.filter(fabric => fabric.collection === collection);
}

